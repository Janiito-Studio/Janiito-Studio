let currentLang = localStorage.getItem('language') || 'es';

function updateLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    const projectsTitle = document.querySelector('.projects-section h2');
    if (projectsTitle && translations[lang]) {
        projectsTitle.textContent = translations[lang]["projects-title"];
    }

    renderProjects();
}

function renderProjects() {
    const grid = document.getElementById('projects-grid');
    if (!grid) return;
    grid.innerHTML = '';

    projects.forEach((project, index) => {
        const card = document.createElement('a');
        card.href = project.url;
        card.target = "_blank";
        card.className = 'project-card stagger-in';
        card.style.transitionDelay = `${index * 0.12}s`;

        const title = project.titles[currentLang] || project.titles['en'];
        const description = project.descriptions[currentLang] || project.descriptions['en'];
        const tags = project.tags[currentLang] || project.tags['en'];

        card.innerHTML = `
            <div class="project-image">
                <img src="${project.image}" alt="${title}">
            </div>
            <div class="project-info">
                <h3>${title}</h3>
                <p>${description}</p>
                <div class="project-tags">
                    ${tags.map(tag => `<span class="tag">${tag}</span>`).join('')}
                </div>
            </div>
        `;

        grid.appendChild(card);
    });

    // Re-trigger observer for new elements
    observeElements();
}

// Intersection Observer for animations
function observeElements() {
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, observerOptions);

    document.querySelectorAll('.fade-in, .stagger-in, section').forEach(el => {
        if (!el.classList.contains('fade-in') && !el.classList.contains('stagger-in')) {
            el.classList.add('fade-in');
        }
        observer.observe(el);
    });
}

// Theme Logic
const themeToggle = document.getElementById('theme-toggle');
const body = document.body;

function toggleTheme() {
    body.classList.toggle('dark-mode');
    const isDark = body.classList.contains('dark-mode');
    localStorage.setItem('theme', isDark ? 'dark' : 'light');
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    // Set initial theme
    if (localStorage.getItem('theme') === 'dark') {
        body.classList.add('dark-mode');
    }

    // Set initial language
    const langSelect = document.getElementById('language-select');
    if (langSelect) {
        langSelect.value = currentLang;
        updateLanguage(currentLang);
    }

    // Initial Observation
    observeElements();

    // Event Listeners
    if (themeToggle) {
        themeToggle.addEventListener('click', toggleTheme);
    }
    if (langSelect) {
        langSelect.addEventListener('change', (e) => updateLanguage(e.target.value));
    }
});
